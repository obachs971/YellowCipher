            logoutput = logoutput + "" + n1 + " " + n2;
            int l1 = ((numbers[0] * n1) + (numbers[1] * n2)) % 26;
            int l2 = ((numbers[2] * n1) + (numbers[3] * n2)) % 26;
            logoutput = logoutput + " -> " + l1 + " " + l2;
            if (l1 == 0)
                l1 = 26;
