        {
            int num = (aa * numbers[0]) - (numbers[1] * numbers[2]);
            if ((num % 2 == 0) || (num % 13 == 0))
                nums = nums.Where(val => val != aa).ToArray();
        }
        
        numbers[3] = nums[UnityEngine.Random.Range(0, nums.Length)];
